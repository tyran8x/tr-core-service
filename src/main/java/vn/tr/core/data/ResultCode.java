package vn.tr.core.data;

import lombok.AllArgsConstructor;
import lombok.NoArgsConstructor;

import java.io.Serializable;

@AllArgsConstructor
@NoArgsConstructor
public enum ResultCode implements IResultCode, Serializable {

	SUCCESS("00000", "Thành công"),

	USER_ERROR("A0001", "USER_ERROR"),
	REPEAT_SUBMIT_ERROR("A0002", "REPEAT_SUBMIT_ERROR"),

	USER_LOGIN_ERROR("A0200", "Đăng nhập thất bại"),

	USER_NOT_EXIST("A0201", "USER_NOT_EXIST"),
	USER_ACCOUNT_LOCKED("A0202", "USER_ACCOUNT_LOCKED"),
	USER_ACCOUNT_INVALID("A0203", "USER_ACCOUNT_INVALID"),

	USERNAME_OR_PASSWORD_ERROR("A0210", "USERNAME_OR_PASSWORD_ERROR"),
	PASSWORD_ENTER_EXCEED_LIMIT("A0211", "PASSWORD_ENTER_EXCEED_LIMIT"),
	CLIENT_AUTHENTICATION_FAILED("A0212", "CLIENT_AUTHENTICATION_FAILED"),

	VERIFY_CODE_TIMEOUT("A0213", "VERIFY_CODE_TIMEOUT"),
	VERIFY_CODE_ERROR("A0214", "VERIFY_CODE_ERROR"),

	TOKEN_INVALID("A0230", "TOKEN_INVALID"),
	TOKEN_ACCESS_FORBIDDEN("A0231", "TOKEN_ACCESS_FORBIDDEN"),

	AUTHORIZED_ERROR("A0300", "AUTHORIZED_ERROR"),
	ACCESS_UNAUTHORIZED("A0301", "ACCESS_UNAUTHORIZED"),
	FORBIDDEN_OPERATION("A0302", "FORBIDDEN_OPERATION"),

	PARAM_ERROR("A0400", "PARAM_ERROR"),
	RESOURCE_NOT_FOUND("A0401", "RESOURCE_NOT_FOUND"),
	PARAM_IS_NULL("A0410", "PARAM_IS_NULL"),

	USER_UPLOAD_FILE_ERROR("A0700", "USER_UPLOAD_FILE_ERROR"),
	USER_UPLOAD_FILE_TYPE_NOT_MATCH("A0701", "USER_UPLOAD_FILE_TYPE_NOT_MATCH"),
	USER_UPLOAD_FILE_SIZE_EXCEEDS("A0702", "USER_UPLOAD_FILE_SIZE_EXCEEDS"),
	USER_UPLOAD_IMAGE_SIZE_EXCEEDS("A0703", "USER_UPLOAD_IMAGE_SIZE_EXCEEDS"),

	SYSTEM_EXECUTION_ERROR("B0001", "SYSTEM_EXECUTION_ERROR"),
	SYSTEM_EXECUTION_TIMEOUT("B0100", "SYSTEM_EXECUTION_TIMEOUT"),
	SYSTEM_ORDER_PROCESSING_TIMEOUT("B0100", "SYSTEM_ORDER_PROCESSING_TIMEOUT"),

	SYSTEM_DISASTER_RECOVERY_TRIGGER("B0200", "SYSTEM_DISASTER_RECOVERY_TRIGGER"),
	FLOW_LIMITING("B0210", "FLOW_LIMITING"),
	DEGRADATION("B0220", "DEGRADATION"),

	SYSTEM_RESOURCE_ERROR("B0300", "SYSTEM_RESOURCE_ERROR"),
	SYSTEM_RESOURCE_EXHAUSTION("B0310", "SYSTEM_RESOURCE_EXHAUSTION"),
	SYSTEM_RESOURCE_ACCESS_ERROR("B0320", "SYSTEM_RESOURCE_ACCESS_ERROR"),
	SYSTEM_READ_DISK_FILE_ERROR("B0321", "SYSTEM_READ_DISK_FILE_ERROR"),

	CALL_THIRD_PARTY_SERVICE_ERROR("C0001", "CALL_THIRD_PARTY_SERVICE_ERROR"),
	MIDDLEWARE_SERVICE_ERROR("C0100", "MIDDLEWARE_SERVICE_ERROR"),
	INTERFACE_NOT_EXIST("C0113", "INTERFACE_NOT_EXIST"),

	MESSAGE_SERVICE_ERROR("C0120", "MESSAGE_SERVICE_ERROR"),
	MESSAGE_DELIVERY_ERROR("C0121", "MESSAGE_DELIVERY_ERROR"),
	MESSAGE_CONSUMPTION_ERROR("C0122", "MESSAGE_CONSUMPTION_ERROR"),
	MESSAGE_SUBSCRIPTION_ERROR("C0123", "MESSAGE_SUBSCRIPTION_ERROR"),
	MESSAGE_GROUP_NOT_FOUND("C0124", "MESSAGE_GROUP_NOT_FOUND"),

	DATABASE_ERROR("C0300", "DATABASE_ERROR"),
	DATABASE_TABLE_NOT_EXIST("C0311", "DATABASE_TABLE_NOT_EXIST"),
	DATABASE_COLUMN_NOT_EXIST("C0312", "DATABASE_COLUMN_NOT_EXIST"),
	DATABASE_DUPLICATE_COLUMN_NAME("C0321", "DATABASE_DUPLICATE_COLUMN_NAME"),
	DATABASE_DEADLOCK("C0331", "DATABASE_DEADLOCK"),
	DATABASE_PRIMARY_KEY_CONFLICT("C0341", "DATABASE_PRIMARY_KEY_CONFLICT");

	private String code;
	private String msg;

	public static ResultCode getValue(String code) {
		for (ResultCode value : values()) {
			if (value.getCode().equals(code)) {
				return value;
			}
		}
		return SYSTEM_EXECUTION_ERROR; // 默认系统执行错误
	}

	@Override
	public String getCode() {
		return code;
	}

	@Override
	public String getMsg() {
		return msg;
	}

	@Override
	public String toString() {
		return "{" +
				"\"code\":\"" + code + '\"' +
				", \"msg\":\"" + msg + '\"' +
				'}';
	}
}
